package me.yu.ato.translate.bean;

/**
 * @author yuxiangxin
 * @since 2021-05-04
 */
public class KeyValue<K, V> {
    private K name;
    private V value;

    public KeyValue() {
    }

    public KeyValue(K key, V value) {
        this.name = key;
        this.value = value;
    }

    public K getName() {
        return name;
    }

    public void setName(K name) {
        this.name = name;
    }

    public V getValue() {
        return value;
    }

    public void setValue(V value) {
        this.value = value;
    }
}
